#!/bin/bash

# Читаем состояние GPIO 2
gpio_value=$(gpio read 1)

# Проверяем, что команда выполнилась успешно (код возврата 0)
if [ $? -ne 0 ]; then
    echo "Ошибка: не удалось выполнить команду 'gpio read 2'" >&2
    exit 1
fi

# Проверяем значение GPIO: если 0, завершаем скрипт с кодом 1
if [ "$gpio_value" -eq 0 ]; then
    exit 1
fi

# Если значение не 0, скрипт завершается с кодом 0 (успех)
exit 0
